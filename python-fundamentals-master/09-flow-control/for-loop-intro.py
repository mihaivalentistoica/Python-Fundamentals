animals = ["Dog", "Cat", "Fish"]

# Print out all animals in the list
for animal in animals:
    print(animal)  # prints out a single animal

# Print 0, 1, 2
for i in range(3):
    print(i)

# Print -3, -2, -1, 0
for i in range(-3, 1):
    print(i)

# Print 0, 3, 5, 7, 9
for i in range(3, 11, 2):
    print(i)

# Print -1, -2, -3
for i in range(-1, -4, -1):
    print(i)
