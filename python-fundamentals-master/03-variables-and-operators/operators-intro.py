# Arithmetic operators
print("Arithmetic operators")
print(1 + 2 + 5 - (2 * 2))
print(501.0 - 99.9999)
print(2 ** 3)
print(10.0 / 4.0)
print(10.0 // 4.0)
print(5 % 2)

# Strings concatenation (joining)
name = "John"
greeting = "Hello, " + name
# Prints ‘Hello, John’
print(greeting)

# String repeat
message = "Hey,ho"
# Prints 'Hey,hoHey,ho'
print(message * 2)

# Assignment operators
print("Assignment operators")
num = 3
num += 2
print(num)
num -= 1
print(num)
num *= 3
print(num)
num /= 2
print(num)
num **= 3
print(num)
num //= 2
print(num)

# Comparison operators
print("Comparison operators")
john_1 = "John"
john_2 = "John"
print(john_1 == john_2)
print(1 != 1)
print(99 < 1.1)
print(99 > 1.1)
print(-32 <= -33)
print(123 >= 123)

# Logical operators
print("Logical operators")
print(True or False)
print(False and False and True)
print(not False)
is_greater = 40 > 30
print(not is_greater)

# Membership operators
print("Membership operators")
print("fox" not in "cow, sheep, dog")
print("wine" in "France is famous for its wines")
